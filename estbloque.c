int estBloque(char board[], char pionAdverse) {
    for (int i = 0; i < taille; i++) {
        if (board[i] == pionAdverse) {
            // Vérifier si le pion peut se déplacer vers une position adjacente vide
            for (int j = 0; j < taille; j++) {
                if (sontAdjacents11(i, j) && board[j] == '*') {
                    return 0; // Le pion peut se déplacer, l'adversaire n'est pas bloqué
                }
            }
        }
    }
    return 1; // Aucun mouvement possible, l'adversaire est bloqué
}

